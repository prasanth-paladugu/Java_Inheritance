//NUMBERS CLASS FOR EX5 ON HIREACHICAL INHERITANCE
public class NUMBERS							//PARENT CLASS
{
	protected int x,y;							//DECLARING VARIABLES
	public void setX(int x)						//METHODS FOR SETTING DATA
	{
		this.x=x;
	}
	public void setY(int y)
	{
		this.y=y;
	}
	public int getX()							//METHODS FOR GETTING DATA
	{
		return this.x;
	}
	public int getY()
	{
		return this.y;
	}
}